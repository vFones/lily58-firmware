static bool logo_scroll_started = false;
static uint32_t logo_scroll_resume_at = 0;
static const uint8_t LOGO_SCROLL_SPEED = 4; // 0 fastest, 7 slowest

void render_atom_logo_full(void) {
    uint32_t now = timer_read32();

    if (logo_scroll_resume_at != 0 && now < logo_scroll_resume_at) {
        if (is_oled_scrolling()) {
            oled_scroll_off();
        }
        logo_scroll_started = false;
        oled_clear();
        oled_write_raw_P(atom_logo, OLED_FRAME_BYTES);
        oled_render_dirty(true);
        return;
    }

    if (!is_oled_scrolling() || !logo_scroll_started) {
        oled_scroll_off();
        oled_clear();
        oled_write_raw_P(atom_logo, OLED_FRAME_BYTES);
        oled_render_dirty(true);
        oled_scroll_set_area(0, (OLED_DISPLAY_HEIGHT / 8) - 1);
        oled_scroll_set_speed(LOGO_SCROLL_SPEED);
        oled_scroll_left();
        logo_scroll_started = true;
    }
}

void pause_logo_scroll(uint16_t pause_ms) {
    logo_scroll_resume_at = timer_read32() + pause_ms;
    logo_scroll_started = false;
    oled_scroll_off();
}
